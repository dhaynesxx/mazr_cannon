/// w = alignments 1-up, 2-down, 3-towardsUser, 4-awayFromUser, 5-toLeft, 6-toRight
app.createGrid = function(x,y,z,w) {
    var grid = [];
    var posx, posy, posz, xmax, ymax, zmax, inc = 10;
    switch (w) {
        case 1:  // up direction
            posx = x - 195; posy = y + 10; posz = z - 195; xmax = x + 195; ymax = y + 10; zmax = z + 195;
            break;
        case 2:  // down direction
            posx = x - 195; posy = y - 10; posz = z - 195; xmax = x + 195; ymax = y - 10; zmax = z + 195;
            break;
        case 3:  // towards user
            posx = x - 195; posy = y - 195; posz = z + 10; xmax = x + 195; ymax = y + 195; zmax = z + 10;
            break;
        case 4:  // away from user
            posx = x - 195; posy = y - 195; posz = z - 10; xmax = x + 195; ymax = y + 195; zmax = z - 10;
            break;
        case 5:  // to left
            posx = x - 10; posy = y - 195; posz = z - 195; xmax = x - 10; ymax = y + 195; zmax = z + 195;
            break;
        case 6:  // to right
            posx = x + 10; posy = y - 195; posz = z - 195; xmax = x + 10; ymax = y + 195; zmax = z + 195;
            break;
    }
    for ( var i = posx; i <= xmax; i += inc ) {
        for ( var j = posy; j <= ymax; j += inc) {
            for (var k = posz; k <= zmax; k += inc) {
                grid.push( [i, j, k] );
            }
        }
    }
    return grid;
};

