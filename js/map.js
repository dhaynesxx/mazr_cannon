app.getWall = function(mapArray, refArray, gridArray) {
        for (var j = 0; j < refArray.length; j++) {
            var resultArray = [];
            for (var i = 0; i < mapArray.length; i++) {
                if(mapArray[i] === refArray[j]) {
                    resultArray.push( gridArray[i]);
                }
            }
            var first = resultArray[0];
            var last = resultArray[ resultArray.length - 1 ];
            app.mapPos.push([first[0] - (first[0]-last[0])/2, first[1] - (first[1]-last[1])/2, first[2] - (first[2]-last[2])/2 ]);
            app.mapSize.push([Math.abs(first[0] - last[0]) + 10, Math.abs(first[1] - last[1]) + 10, Math.abs(first[2] - last[2]) + 10 ]);
        }
};

